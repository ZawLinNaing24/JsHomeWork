let situation = "morning"
switch (situation) {
    case "morining":
        console.log("Eat Breakfast and Prepare to go work.");
        break;
    case "Afternoon":
        console.log("Eat Lunch and take a rest for a few minutes and then Continue to do work");
        break;
    case "Evening":
        console.log("It is time to go back home");
        break;
    case "Night":
        console.log("Eat Dinner, take a rest and then go to sleep");
        break;
    default:
        console.log("I am sleeping now Zzz");
        break;
}